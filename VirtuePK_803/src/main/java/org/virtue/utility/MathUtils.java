package org.virtue.utility;

/**
 * @author Taylor
 * @version 1.0
 */
public class MathUtils {
	
	/**
	 * Returns the percent of two integers.
	 * @param i The first integer.
	 * @param v The second integer.
	 * @return The percent.
	 */
	public static int percentOf(int i, int v) {
		return (i * v) / 100;
	}
}
